
x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0

print(type(x))
print(type(y))


# Integer
x = int(1)     # x will be 1
y = int(2.8)   # y will be 2
z = int("3")   # z will be 3
print(x)
print(y)
print(z)


# Float
x = float(1)         # x will be 1.0
y = float(2.8)       # y will be 2.8
z = float("3")       # z will be 3.0
w = float("4.2")     # w will be 4.2

print(x)
print(y)
print(z)
print(w)


# String
x = str("s1")       # x will be 's1'
y = str(2)          # x will be '2'
z = str(3.0)       # x will be '3.0'

print(x)
print(y)
print(z)